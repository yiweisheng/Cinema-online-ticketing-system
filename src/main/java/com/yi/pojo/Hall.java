package com.yi.pojo;

public class Hall {
    private String Hall_ID;            //影厅ID
    private String Hall_Name;           //影厅名
    private int Hall_Seats;             //影厅座位数
    private String Hall_Description;    //影厅描述

    public String getHall_ID() {return Hall_ID;}
    public void setHall_ID(String hall_ID) {Hall_ID = hall_ID;}

    public String getHall_Name() {return Hall_Name;}
    public void setHall_Name(String hall_Name) {Hall_Name = hall_Name;}

    public int getHall_Seats() {return Hall_Seats;}
    public void setHall_Seats(int hall_Seats) {Hall_Seats = hall_Seats;}

    public String getHall_Description() {return Hall_Description;}
    public void setHall_Description(String hall_Description) {Hall_Description = hall_Description;}
}
